package com.loop.demo;

public class If_ {
	public static void main(String[] args) {
		int i = 100;

		if (i >= 30) {
			System.out.println("fail");
			

		}
		else if (i <= 60) {
			System.out.println("grade c");
			
		} 
		else if (i <= 80) {
			System.out.println("grade b");
			
		}
		else if (i <= 90) {
			System.out.println("grade a");
			
			
		}
		else if (i <= 100) {
			System.out.println("grade a+");
			
		}
		else {
			System.out.println("absent");
			
		}
			

		}

	}

